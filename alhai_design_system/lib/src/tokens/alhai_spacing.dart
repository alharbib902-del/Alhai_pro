/// Spacing tokens for Alhai Design System
/// Based on 4px grid system
abstract final class AlhaiSpacing {
  // ============================================
  // Base Unit
  // ============================================

  /// Base spacing unit (4dp)
  static const double unit = 4.0;

  // ============================================
  // Spacing Scale
  // ============================================

  /// 0dp - No spacing
  static const double zero = 0.0;

  /// 2dp - Hairline spacing
  static const double xxxs = 2.0;

  /// 4dp - Extra extra small
  static const double xxs = 4.0;

  /// 8dp - Extra small
  static const double xs = 8.0;

  /// 12dp - Small
  static const double sm = 12.0;

  /// 16dp - Medium (default)
  static const double md = 16.0;

  /// 20dp - Medium-large
  static const double mdl = 20.0;

  /// 24dp - Large
  static const double lg = 24.0;

  /// 32dp - Extra large
  static const double xl = 32.0;

  /// 40dp - Extra extra large
  static const double xxl = 40.0;

  /// 48dp - Extra extra extra large
  static const double xxxl = 48.0;

  /// 56dp - Huge
  static const double huge = 56.0;

  /// 64dp - Massive
  static const double massive = 64.0;

  // ============================================
  // Semantic Spacing
  // ============================================

  /// Page horizontal padding
  static const double pagePaddingHorizontal = 16.0;

  /// Page vertical padding
  static const double pagePaddingVertical = 24.0;

  /// Card internal padding
  static const double cardPadding = 16.0;

  /// Section spacing (between sections)
  static const double sectionSpacing = 24.0;

  /// Item spacing (between list items)
  static const double itemSpacing = 12.0;

  /// Input field spacing
  static const double inputSpacing = 16.0;

  /// Button content padding horizontal
  static const double buttonPaddingHorizontal = 24.0;

  /// Button content padding vertical
  static const double buttonPaddingVertical = 12.0;

  /// Icon-text gap
  static const double iconTextGap = 8.0;

  /// Minimum touch target (accessibility)
  static const double minTouchTarget = 48.0;

  // ============================================
  // Component-specific
  // ============================================

  /// Bottom sheet drag handle width
  static const double dragHandleWidth = 32.0;

  /// Bottom sheet drag handle height
  static const double dragHandleHeight = 4.0;

  /// Dialog minimum width
  static const double dialogMinWidth = 280.0;

  /// Dialog maximum width
  static const double dialogMaxWidth = 400.0;

  /// Loading indicator stroke width
  static const double strokeSm = 2.0;

  /// Extra small stroke (1dp) for card borders
  static const double strokeXs = 1.0;

  /// Icon size for dialogs/alerts
  static const double iconLg = 48.0;

  /// List tile standard min height
  static const double listTileMinHeight = 56.0;

  /// List tile compact min height
  static const double listTileCompactMinHeight = 44.0;

  /// Bottom navigation bar height
  static const double bottomNavHeight = 80.0;

  // ============================================
  // Avatar sizes
  // ============================================

  /// Avatar XS (24dp)
  static const double avatarXs = 24.0;

  /// Avatar SM (32dp)
  static const double avatarSm = 32.0;

  /// Avatar MD (40dp)
  static const double avatarMd = 40.0;

  /// Avatar LG (56dp)
  static const double avatarLg = 56.0;

  /// Avatar XL (80dp)
  static const double avatarXl = 80.0;

  /// Online dot size
  static const double onlineDotSize = 12.0;

  // ============================================
  // Avatar font sizes (for initials)
  // ============================================

  /// Avatar font XS
  static const double avatarFontXs = 10.0;

  /// Avatar font SM
  static const double avatarFontSm = 12.0;

  /// Avatar font MD
  static const double avatarFontMd = 14.0;

  /// Avatar font LG
  static const double avatarFontLg = 20.0;

  /// Avatar font XL
  static const double avatarFontXl = 28.0;

  // ============================================
  // Avatar icon sizes
  // ============================================

  /// Avatar icon XS
  static const double avatarIconXs = 14.0;

  /// Avatar icon SM
  static const double avatarIconSm = 18.0;

  /// Avatar icon MD
  static const double avatarIconMd = 22.0;

  /// Avatar icon LG
  static const double avatarIconLg = 28.0;

  /// Avatar icon XL
  static const double avatarIconXl = 40.0;
}
